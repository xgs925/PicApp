public class TJFaceLayer extends TJMaterialLayer {
    private String leftEyeCenterX;
    private String leftEyeCenterY;
    private String rightEyeCenterX;
    private String rightEyeCenterY;
    private String faceWidth;
    private String faceHeight;

    public String getLeftEyeCenterX() {
        return leftEyeCenterX;
    }

    public void setLeftEyeCenterX(String leftEyeCenterX) {
        this.leftEyeCenterX = leftEyeCenterX;
    }

    public String getLeftEyeCenterY() {
        return leftEyeCenterY;
    }

    public void setLeftEyeCenterY(String leftEyeCenterY) {
        this.leftEyeCenterY = leftEyeCenterY;
    }

    public String getRightEyeCenterX() {
        return rightEyeCenterX;
    }

    public void setRightEyeCenterX(String rightEyeCenterX) {
        this.rightEyeCenterX = rightEyeCenterX;
    }

    public String getRightEyeCenterY() {
        return rightEyeCenterY;
    }

    public void setRightEyeCenterY(String rightEyeCenterY) {
        this.rightEyeCenterY = rightEyeCenterY;
    }

    public String getFaceWidth() {
        return faceWidth;
    }

    public void setFaceWidth(String faceWidth) {
        this.faceWidth = faceWidth;
    }

    public String getFaceHeight() {
        return faceHeight;
    }

    public void setFaceHeight(String faceHeight) {
        this.faceHeight = faceHeight;
    }
}
